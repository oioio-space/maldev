package crypto

import "fmt"

// AES key expansion — pure Go implementation of FIPS 197 § 5.2.
//
// Why this lives next to [EncryptAESCTR] / [EncryptAESGCM] rather than
// relying on `crypto/aes`: Go stdlib's `aes.NewCipher` performs the
// expansion internally but does NOT expose the resulting round keys.
// The maldev all-asm bundle stub needs the expanded keys *in the
// bundle wire format* so the AES-NI decrypt loop (Phase 3 of Tier 🟡
// #2.2) can MOVDQU them into XMM at runtime without a software
// expansion step at decrypt time.
//
// The implementation follows the algorithm in FIPS 197 — RotWord,
// SubWord (via the AES forward S-box), and XOR with a per-round
// constant Rcon. Test vectors are pinned against Appendix A.1 of
// the standard (key = 2b7e151628aed2a6abf7158809cf4f3c).

// aesSBox is the AES forward S-box (FIPS 197 Fig. 7). Used by
