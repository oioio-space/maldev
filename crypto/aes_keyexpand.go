// aesRcon are the round constants (FIPS 197 § 5.2). Only the high
// byte of each 4-byte word is non-zero; the rest are zero. Index 0
// is unused (the spec numbers Rcon from 1).
var aesRcon = [11]byte{0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36}

// ExpandAESKey expands a 16-byte AES-128 key into 11 × 16-byte
// round keys (176 bytes total) per FIPS 197 § 5.2 Figure 11.
//
// The output is laid out contiguously: round key 0 is the input
// key itself at offset 0, round key 1 at offset 16, …, round key
// 10 at offset 160. This matches the layout expected by the all-
// asm bundle stub's AES-CTR decrypt path
// (`emitAESCTRBlockDecrypt`, Tier 🟡 #2.2 Phase 3a) which reads
// each round key via `MOVDQU XMM, [R8 + 16*round]`.
//
// Only AES-128 is supported — bundle wire format reserves a single
// 16-byte key slot per PayloadEntry. AES-192 / AES-256 would need
// a wider key field; not planned.
func ExpandAESKey(key []byte) ([]byte, error) {
	if len(key) != 16 {
		return nil, fmt.Errorf("crypto: ExpandAESKey requires 16-byte key, got %d", len(key))
	}
	const (
		Nk = 4  // key length in 32-bit words
		Nb = 4  // block length in 32-bit words
		Nr = 10 // rounds
	)
	// W is the expanded key as Nb*(Nr+1) 32-bit words = 44 words.
	var w [Nb * (Nr + 1)][4]byte
	for i := 0; i < Nk; i++ {
		copy(w[i][:], key[4*i:4*(i+1)])
	}
	for i := Nk; i < Nb*(Nr+1); i++ {
		temp := w[i-1]
		if i%Nk == 0 {
			// RotWord (cyclic left shift by 1 byte) + SubWord +
			// XOR with Rcon[i/Nk].
			temp = [4]byte{
				aesSBox[temp[1]] ^ aesRcon[i/Nk],
				aesSBox[temp[2]],
				aesSBox[temp[3]],
				aesSBox[temp[0]],
			}
		}
		for j := 0; j < 4; j++ {
			w[i][j] = w[i-Nk][j] ^ temp[j]
		}
	}
	out := make([]byte, Nb*(Nr+1)*4)
	for i := range w {
		copy(out[4*i:4*(i+1)], w[i][:])
	}
	return out, nil
}
